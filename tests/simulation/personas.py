"""
50 diverse user personas for conversational simulation.

Six behavioural groups:
  GROUP A — Friendly / Cooperative   (10 personas)
  GROUP B — Difficult / Demanding    (10 personas)
  GROUP C — Confused / Vague         (10 personas)
  GROUP D — Human-quirk Realists     (10 personas)
  GROUP E — Adversarial              (5  personas)
  GROUP F — Edge-case Specialists    (5  personas)
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Optional


@dataclass
class Persona:
    id: str
    name: str
    age: int
    background: str
    traits: list[str]
    style: str               # "casual"|"formal"|"terse"|"verbose"|"fragmented"|"chaotic"
    grammar: str             # "perfect"|"good"|"average"|"poor"|"atrocious"
    typo_rate: float         # 0.0–1.0
    emoji_rate: float        # 0.0–1.0
    patience: str            # "high"|"medium"|"low"|"zero"
    confidence: str          # "high"|"medium"|"low"|"none"
    group: str               # A–F
    adversarial: bool
    openers: list[str]       # Possible first messages (8–12 variants)
    mid_phrases: list[str]   # Mid-conversation filler phrases
    confirm_phrases: list[str]   # Agreement / confirmation phrases
    pushback_phrases: list[str]  # Disagreement / frustration phrases
    abandonment_threshold: int   # Friction turns before they quit (1–10)


